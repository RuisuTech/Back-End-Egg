public class SumaElementosArray {
    public static void main(String[] args) {
        // Crear un array de enteros con tres elementos
        int[] array = {5, 10, 15};

        // Sumar los elementos del array manualmente
        int suma = array[0] + array[1] + array[2];

        // Imprimir el resultado por consola
        System.out.println("La suma de los elementos del array es: " + suma);
    }
}
